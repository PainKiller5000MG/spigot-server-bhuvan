    public static List<CreativeModeTab> tabs() {
        return streamAllTabs().filter(CreativeModeTab::shouldDisplay).toList();
    }

    public static List<CreativeModeTab> allTabs() {
        return streamAllTabs().toList();
    }

    private static Stream<CreativeModeTab> streamAllTabs() {
        return BuiltInRegistries.CREATIVE_MODE_TAB.stream();
    }

    public static CreativeModeTab searchTab() {
        return BuiltInRegistries.CREATIVE_MODE_TAB.getValueOrThrow(CreativeModeTabs.SEARCH);
    }

    private static void buildAllTabContents(CreativeModeTab.ItemDisplayParameters parameters) {
        streamAllTabs().filter((creativemodetab) -> {
            return creativemodetab.getType() == CreativeModeTab.Type.CATEGORY;
        }).forEach((creativemodetab) -> {
            creativemodetab.buildContents(parameters);
        });
        streamAllTabs().filter((creativemodetab) -> {
            return creativemodetab.getType() != CreativeModeTab.Type.CATEGORY;
        }).forEach((creativemodetab) -> {
            creativemodetab.buildContents(parameters);
        });
    }

    public static boolean tryRebuildTabContents(FeatureFlagSet enabledFeatures, boolean hasPermissions, HolderLookup.Provider lookup) {
        if (CreativeModeTabs.CACHED_PARAMETERS != null && !CreativeModeTabs.CACHED_PARAMETERS.needsUpdate(enabledFeatures, hasPermissions, lookup)) {
            return false;
        } else {
            CreativeModeTabs.CACHED_PARAMETERS = new CreativeModeTab.ItemDisplayParameters(enabledFeatures, hasPermissions, lookup);
            buildAllTabContents(CreativeModeTabs.CACHED_PARAMETERS);
            return true;
        }
    }
