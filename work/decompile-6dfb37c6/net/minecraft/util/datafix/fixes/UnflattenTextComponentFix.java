package net.minecraft.util.datafix.fixes;

import com.google.gson.JsonElement;
import com.mojang.datafixers.DataFix;
import com.mojang.datafixers.TypeRewriteRule;
import com.mojang.datafixers.schemas.Schema;
import com.mojang.datafixers.types.Type;
import com.mojang.datafixers.util.Pair;
import com.mojang.logging.LogUtils;
import com.mojang.serialization.Dynamic;
import com.mojang.serialization.DynamicOps;
import com.mojang.serialization.JsonOps;
import net.minecraft.util.LenientJsonParser;
import net.minecraft.util.Util;
import org.slf4j.Logger;

public class UnflattenTextComponentFix extends DataFix {

    private static final Logger LOGGER = LogUtils.getLogger();

    public UnflattenTextComponentFix(Schema outputSchema) {
        super(outputSchema, true);
    }

    protected TypeRewriteRule makeRule() {
        Type<Pair<String, String>> type = this.getInputSchema().getType(References.TEXT_COMPONENT);
        Type<?> type1 = this.getOutputSchema().getType(References.TEXT_COMPONENT);

        return this.createFixer(type, type1);
    }

    private <T> TypeRewriteRule createFixer(Type<Pair<String, String>> textComponentType, Type<T> newTextComponentType) {
        return this.fixTypeEverywhere("UnflattenTextComponentFix", textComponentType, newTextComponentType, (dynamicops) -> {
            return (pair) -> {
                return Util.readTypedOrThrow(newTextComponentType, unflattenJson(dynamicops, (String) pair.getSecond()), true).getValue();
            };
        });
    }

    private static <T> Dynamic<T> unflattenJson(DynamicOps<T> ops, String jsonString) {
        try {
            JsonElement jsonelement = LenientJsonParser.parse(jsonString);

            if (!jsonelement.isJsonNull()) {
                return new Dynamic(ops, JsonOps.INSTANCE.convertTo(ops, jsonelement));
            }
        } catch (Exception exception) {
            UnflattenTextComponentFix.LOGGER.error("Failed to unflatten text component json: {}", jsonString, exception);
        }

        return new Dynamic(ops, ops.createString(jsonString));
    }
}
